const FONT_BASE = 0x20;
const FONT_HEIGHT = 8;
const FONT_WIDTH = 8;

const CRC_TABLE = (() => {
  const t = new Uint32Array(256);
  for (let i = 0; i < 256; i++) {
    let c = i;
    for (let j = 0; j < 8; j++) c = c & 1 ? 0xedb88320 ^ (c >>> 1) : c >>> 1;
    t[i] = c;
  }
  return t;
})();

function crc32(data) {
  let crc = 0xffffffff;
  for (let i = 0; i < data.length; i++) crc = CRC_TABLE[(crc ^ data[i]) & 0xff] ^ (crc >>> 8);
  return (crc ^ 0xffffffff) >>> 0;
}

function concatU8(...arrays) {
  const total = arrays.reduce((s, a) => s + a.length, 0);
  const out = new Uint8Array(total);
  let off = 0;
  for (const a of arrays) { out.set(a, off); off += a.length; }
  return out;
}

function u32be(n) {
  const b = new Uint8Array(4);
  new DataView(b.buffer).setUint32(0, n, false);
  return b;
}

function pngChunk(type, data) {
  const typeBytes = new TextEncoder().encode(type);
  const crcInput = concatU8(typeBytes, data);
  return concatU8(u32be(data.length), typeBytes, data, u32be(crc32(crcInput)));
}

async function zlibDeflate(input) {
  const cs = new CompressionStream("deflate");
  const writer = cs.writable.getWriter();
  const reader = cs.readable.getReader();
  writer.write(input);
  writer.close();
  const chunks = [];
  let done, value;
  while ({ done, value } = await reader.read(), !done) chunks.push(value);
  return concatU8(...chunks);
}

/**
 * Encodes an RGBA pixel buffer as a PNG ArrayBuffer.
 * @param {number} width
 * @param {number} height
 * @param {Uint8Array} rgba - width*height*4 bytes, row-major
 * @returns {Promise<ArrayBuffer>}
 */
async function encodePNG(width, height, rgba) {
  const sig = new Uint8Array([137, 80, 78, 71, 13, 10, 26, 10]);

  const ihdrData = new Uint8Array(13);
  const dv = new DataView(ihdrData.buffer);
  dv.setUint32(0, width, false);
  dv.setUint32(4, height, false);
  ihdrData[8] = 8; ihdrData[9] = 6;

  const scanlines = new Uint8Array(height * (1 + width * 4));
  for (let y = 0; y < height; y++) {
    scanlines[y * (1 + width * 4)] = 0;
    scanlines.set(rgba.subarray(y * width * 4, (y + 1) * width * 4), y * (1 + width * 4) + 1);
  }

  const compressed = await zlibDeflate(scanlines);
  return concatU8(
    sig,
    pngChunk("IHDR", ihdrData),
    pngChunk("IDAT", compressed),
    pngChunk("IEND", new Uint8Array(0)),
  ).buffer;
}


function isEmoji(cp) {
  return (
    (cp >= 0x1f300 && cp <= 0x1faff) ||
    (cp >= 0x2600  && cp <= 0x27bf)  ||
    (cp >= 0x2300  && cp <= 0x23ff)  ||
    cp === 0x00a9 || cp === 0x00ae
  );
}

/** @returns {Uint8Array|null} 8 bytes of glyph data, or null for emoji/unknown */
function getGlyph(cp) {
  if (cp >= FONT_BASE && cp <= 0x7e) {
    const off = (cp - FONT_BASE) * FONT_HEIGHT;
    return FONT_DATA.subarray(off, off + FONT_HEIGHT);
  }
  return null;
}

function parseHex(hex) {
  return {
    r: parseInt(hex.slice(1, 3), 16),
    g: parseInt(hex.slice(3, 5), 16),
    b: parseInt(hex.slice(5, 7), 16),
  };
}

function setPixel(buf, w, h, x, y, r, g, b, a) {
  if (x < 0 || x >= w || y < 0 || y >= h) return;
  const i = (y * w + x) * 4;
  buf[i] = r; buf[i + 1] = g; buf[i + 2] = b; buf[i + 3] = a;
}

/**
 * Collects "ink" pixel offsets within a scaled character cell.
 * Emoji cells fill the entire box; ASCII uses bitmap font bits.
 */
function inkPixels(glyph, emoji, scale) {
  const pixels = [];
  if (emoji) {
    for (let py = 0; py < FONT_HEIGHT * scale; py++)
      for (let px = 0; px < FONT_WIDTH * scale; px++)
        pixels.push(px, py);
  } else {
    for (let row = 0; row < FONT_HEIGHT; row++) {
      const byte = glyph[row];
      for (let col = 0; col < FONT_WIDTH; col++) {
        if (byte & (1 << col)) {
          for (let sy = 0; sy < scale; sy++)
            for (let sx = 0; sx < scale; sx++)
              pixels.push(col * scale + sx, row * scale + sy);
        }
      }
    }
  }
  return pixels;
}

function drawChar(buf, canvasW, canvasH, glyph, emoji, originX, originY, scale, sw, sc, fc) {
  const ink = inkPixels(glyph, emoji, scale);
  const swSq = sw * sw;

  // Stroke pass (white outline)
  for (let i = 0; i < ink.length; i += 2) {
    const px = ink[i], py = ink[i + 1];
    for (let dy = -sw; dy <= sw; dy++) {
      for (let dx = -sw; dx <= sw; dx++) {
        if (dx * dx + dy * dy <= swSq)
          setPixel(buf, canvasW, canvasH, originX + px + dx, originY + py + dy, sc.r, sc.g, sc.b, 255);
      }
    }
  }
  // Fill pass (black text)
  for (let i = 0; i < ink.length; i += 2)
    setPixel(buf, canvasW, canvasH, originX + ink[i], originY + ink[i + 1], fc.r, fc.g, fc.b, 255);
}


/** Character count of a string, handling surrogate pairs. */
function charCount(str) {
  return [...str].length;
}

/**
 * Splits text into display lines respecting bbox width.
 * Handles explicit `\n` breaks and word-wrapping at spaces.
 * Single words wider than maxWidth are broken mid-character.
 * @param {string} text
 * @param {number} maxWidth - pixels
 * @param {number} charW - pixels per character
 * @returns {string[]}
 */
function wrapLines(text, maxWidth, charW) {
  const result = [];
  for (const para of text.split("\n")) {
    if (para === "") { result.push(""); continue; }
    const words = para.split(" ");
    let line = "";
    for (const word of words) {
      if (charCount(word) * charW > maxWidth && line) {
        // word alone exceeds width — flush current line first
        result.push(line);
        line = "";
      }
      const candidate = line ? line + " " + word : word;
      if (charCount(candidate) * charW > maxWidth && line) {
        result.push(line);
        line = word;
      } else {
        line = candidate;
      }
    }
    if (line) result.push(line);
  }
  return result;
}


/**
 * Renders caption text as a transparent PNG overlay.
 *
 * Canvas dimensions: width = bbox.width, height = number_of_lines × lineHeight.
 * Transparent pixels surround all text (alpha = 0). Text pixels carry the
 * stroke color at the outline and fill color at the glyph interior.
 *
 * @param {Object} opts
 * @param {string} opts.text - Caption text; may contain `\n` for explicit breaks.
 * @param {number} opts.fontSize - Desired font size in pixels (snapped to 8px scale steps).
 * @param {{ x: number, y: number, width: number, height: number }} opts.bbox
 *   Bounding box on the template. `width` drives wrapping; `x`/`y` are for the compositor.
 * @param {"center"|"left"} [opts.align="center"] - Horizontal alignment within bbox.
 * @param {string} [opts.fontFamily] - Ignored (bitmap font only); kept for API stability.
 * @param {number} [opts.strokeWidth=2] - Outline radius in pixels.
 * @param {string} [opts.strokeColor="#FFFFFF"] - CSS hex color for the text outline.
 * @param {string} [opts.fillColor="#000000"] - CSS hex color for the text fill.
 * @returns {Promise<{ png: ArrayBuffer, width: number, height: number }>}
 *   `png` is a valid RGBA PNG. `width`/`height` match the canvas dimensions.
 */
export async function renderTextOverlay({
  text = "",
  fontSize = 24,
  bbox,
  align = "center",
  strokeWidth = 2,
  strokeColor = "#FFFFFF",
  fillColor = "#000000",
}) {
  const scale = Math.max(1, Math.floor(fontSize / FONT_HEIGHT));
  const charW = FONT_WIDTH * scale;
  const charH = FONT_HEIGHT * scale;
  const lineGap = 2;
  const lineHeight = charH + lineGap;
  const sw = strokeWidth;

  const canvasW = bbox.width;
  const lines = text === "" ? [] : wrapLines(text, canvasW, charW);
  // Extra vertical padding so top/bottom stroke is not clipped
  const canvasH = lines.length === 0 ? 1 : lines.length * lineHeight + sw * 2;

  const buf = new Uint8Array(canvasW * canvasH * 4); // all transparent
  const sc = parseHex(strokeColor);
  const fc = parseHex(fillColor);

  for (let li = 0; li < lines.length; li++) {
    const lineChars = [...lines[li]];
    const lineW = lineChars.length * charW;
    const originX = align === "center"
      ? Math.max(0, Math.floor((canvasW - lineW) / 2))
      : 0;
    const originY = sw + li * lineHeight;

    let cx = originX;
    for (const ch of lineChars) {
      const cp = ch.codePointAt(0);
      const emoji = isEmoji(cp);
      const glyph = emoji ? null : getGlyph(cp);
      if (emoji || glyph) {
        drawChar(buf, canvasW, canvasH, glyph, emoji, cx, originY, scale, sw, sc, fc);
      }
      cx += charW;
    }
  }

  const png = await encodePNG(canvasW, canvasH, buf);
  return { png, width: canvasW, height: canvasH };
}
